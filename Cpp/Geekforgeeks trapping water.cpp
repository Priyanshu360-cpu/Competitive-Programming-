class Solution{

    // Function to find the trapped water between the blocks.
    public:
    long long trappingWater(int arr[], int n){
        int max=arr[0];
        int sum=0;
        for(int i=0;i<n;i++){
            if(arr[i]<max){
                sum=sum+(max-arr[i]);
            }
        }
        return sum;
    }
};

